// Convert ../tests/extension-smoke/fixtures.toml into a JS module
// the browser test can iterate. We filter to .scalar entries (the
// browser bundle only registers scalar fns), drop entries whose
// extension isn't in our transpiled set, and drop entries that
// require host capabilities we can't satisfy in the browser
// (`grants = ["dns"]` / `["http"]`).

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { parse } from 'smol-toml'

const __dirname = dirname(fileURLToPath(import.meta.url))
const FIXTURES_TOML = resolve(__dirname, '..', '..', 'tests', 'extension-smoke', 'fixtures.toml')
const FALLBACK_FIXTURES_TOML =
  '/Users/zacharywhitley/git/sqlink/tests/extension-smoke/fixtures.toml'
const OUT = resolve(__dirname, '..', 'src', 'generated', 'fixtures.js')

import { existsSync, readdirSync } from 'node:fs'

function readFixtures() {
  const path = existsSync(FIXTURES_TOML) ? FIXTURES_TOML : FALLBACK_FIXTURES_TOML
  return parse(readFileSync(path, 'utf8'))
}

function transpiledNames() {
  const dir = resolve(__dirname, '..', 'src', 'generated')
  if (!existsSync(dir)) return new Set()
  const entries = readdirSync(dir, { withFileTypes: true })
  return new Set(entries.filter((e) => e.isDirectory()).map((e) => e.name))
}

function main() {
  const toml = readFixtures()
  const available = transpiledNames()
  // Top-level table is `extension`; each key under it is an
  // extension name. Each extension may have nested tables
  // (`.scalar`, etc.) and a top-level `grants` field.
  const ext = toml.extension ?? {}
  const fixtures = []
  for (const [name, entry] of Object.entries(ext)) {
    if (!available.has(name)) continue
    // Skip entries with grants we can't satisfy in the browser.
    if (Array.isArray(entry.grants)) {
      const blocked = entry.grants.some((g) => ['dns', 'http'].includes(g))
      if (blocked) continue
    }
    const scalar = entry.scalar
    if (!scalar?.sql) continue
    fixtures.push({
      extension: name,
      sql: scalar.sql,
      expects: scalar.expects,
      expects_regex: scalar.expects_regex,
    })
  }

  mkdirSync(dirname(OUT), { recursive: true })
  writeFileSync(
    OUT,
    `// Auto-generated by scripts/build-fixtures.mjs. Do not edit by hand.\nexport const FIXTURES = ${JSON.stringify(
      fixtures,
      null,
      2,
    )}\n`,
  )
  console.log(`[fixtures] wrote ${fixtures.length} scalar fixtures`)
}

main()
